# For Loop
print("For Loop:")
for i in range(1, 6):
    print(i, end=" ")
print("\n")

# While Loop
print("While Loop:")
count = 1
while count <= 5:
    print(count, end=" ")
    count += 1
print("\n")

# Do-While Loop (Simulated)
print("Do-While Loop (Simulated):")
count = 1
while True:
    print(count, end=" ")
    count += 1
    if count > 5:
        break
print("\n")